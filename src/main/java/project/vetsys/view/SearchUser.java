    private void loadUsersByClinic(int clinicId) {
        UserDAO userDAO = new UserDAO();
        List<User> users = userDAO.Read(clinicId);

        // Modelo de la tabla
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Usuario");
        model.addColumn("Rol");
        model.addColumn("Estado");
        model.addColumn("ClÃ­nica");

        // Llenar filas
        for (User user : users) {
            model.addRow(new Object[]{
                user.getId_user(),
                user.getUsername(),
                user.getName_role(),
                user.getName_status(),
                user.getClinic().getName_clinic()
            });
        }

        userTable.setModel(model);
    }
    
